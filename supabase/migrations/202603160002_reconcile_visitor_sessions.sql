  add column if not exists created_at timestamptz,
  add column if not exists consent_status text,
  add column if not exists latitude double precision,
  add column if not exists longitude double precision,
  add column if not exists accuracy_meters integer,
  add column if not exists coarse_country text,
  add column if not exists coarse_region text,
  add column if not exists coarse_city text,
